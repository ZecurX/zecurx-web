/**
 * Escape HTML special characters to prevent HTML injection in email templates.
 */
export function escapeHtml(str: unknown): string {
  const s = String(str ?? '');
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#x27;');
}
